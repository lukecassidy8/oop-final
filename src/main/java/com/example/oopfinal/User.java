package com.example.oopfinal;

/**
 * The type User.
 */
public class User {
    private String username;

    /**
     * Instantiates a new User.
     *
     * @param username the username
     */
    public User(String username){
        this.username = username;
    }

    /**
     * Gets username.
     *
     * @return the username
     */
    public String getUsername() {
        return username;
    }
}
